package com.xray.common.reference;

public class OutlineColor {
    private int r = 0;
    private int g = 0;
    private int b = 0;

    public OutlineColor(int r, int g, int b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public int getRed() { return this.r; }
    public int getGreen() { return this.g; }
    public int getBlue() { return this.b; }
}
